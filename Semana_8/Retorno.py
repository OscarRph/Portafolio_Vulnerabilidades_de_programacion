#Funciones con retorno
def entero():
    print('Este es un dato entero: ')
    return 10

entero()
print(entero())

def decimal():
    print('Este es un dato decimal: ')
    return 10.99

decimal()
print(decimal())

def frase():
    return 'xd'

frase()
print(frase())

