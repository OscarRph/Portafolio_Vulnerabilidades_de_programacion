#Funciones Propias de Python Ejemplos


# Funcion Type // devuelve el tipo de dato de una variable
num = "70"
print(type(num))

#Funcion int // vuelve entero al dato
print(type(int(num)))

#Funcion len // Devuelve el numero de datos de una lista
lista = [12, 60, 70, 1, -2]
print(len(lista))

#Funcion max y min que devuelven los elementos mas altos y bajos dentro de una lista
print(max(lista))
print(min(lista))