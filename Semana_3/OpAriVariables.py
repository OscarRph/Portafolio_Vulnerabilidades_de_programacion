num = 10
num2 = 5


#Forma de usar los operadores aritmeticos usando dos variables
#Suma
print(num + num2)
#Resta
print(num-num2)
#Multiplicación
print(num*num2)
#División
print(num/num2)
#Exponensiación
print(num**num2) 
#División entera
print(num//num2)
#Modulo
print(num%num2)

