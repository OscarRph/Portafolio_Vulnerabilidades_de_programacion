#Tipo de datos numéricos en Python

#Dato tipo entero
num1 = 40
#Demostracion del tipo de dato
print(type(num1))
#El siguiuente comando transforma el tipo de dato a flotante y lo muestra en consola
print(type(float(num1)))

#Dato tipo flotante
num2 = 40.999
#Demostracion del tipo de dato
print(type(num1))
#El siguiuente comando transforma el tipo de dato a entero y lo muestra en consola
print(type(int(num2)))

#Nota: cuando cambias de flotante a entero, python no redondea el numero, solo utiliza la parte entera del número.
