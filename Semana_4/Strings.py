#Creacion de una cadena
cadena = 'Esto es un "ejemplo" de cadena de texto'

print (cadena)

#Escapar caracteres
cadena = "Esto es un \"ejemplo\" de cadena de texto"

print (cadena)

cadena = 'Esto es un \'ejemplo\' de cadena de texto'

print (cadena)

cadena = "Esto es un \nejemplo de cadena de texto"

print (cadena)

cadena = "Esto es un \tejemplo de cadena de texto"

print (cadena)

